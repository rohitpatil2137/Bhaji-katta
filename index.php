        <p class="sectionTag text-big">Harvesting </p>
        <p class="sectionSubTag text-small">When a vegetable is harvested, it is cut off from its source of water and nourishment. It continues to transpire and loses moisture as it does so, a process most noticeable in the wilting of green leafy crops.
             Harvesting root vegetables when they are fully mature improves their storage life, but alternatively, these root crops can be left in the ground and harvested over an extended period.
              The harvesting process should seek to minimise damage and bruising to the crop. Onions and garlic can be dried for a few days in the field and root crops such as potatoes benefit from a short maturation period in warm, moist surroundings, during which time wounds heal and the skin thickens up and hardens.
             Before marketing or storage, grading needs to be done to remove damaged goods and select produce according to its quality, size, ripeness, and color.</p>
